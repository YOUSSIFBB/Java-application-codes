    private void deleteLastIndexBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteLastIndexBtnActionPerformed
        // TODO add your handling code here:
        if(study.isEmpty()){
            //print this message if the arrayList is empty
            JOptionPane.showMessageDialog(null, "unable to delete the last index, the data base is empty"); 
        }else{
            
            for(int i = 0; i < study.size(); i++){
                Study s = study.get(study.size()-1); //select the last index of the array, the most recently added
                study.remove(s);
                JOptionPane.showMessageDialog(null, "Last data entered has been deleted successfully");
                break;  //break the loop to i does not delete twice, error solved
                
            }//for loop
        
        }//else
    }//GEN-LAST:event_deleteLastIndexBtnActionPerformed

