    public void AutoLoad(){
        //this method here will automatically load and print out the data when running the application, you can alos press on the load button to load the data manually
        File f; 
        FileInputStream inputStream;
        ObjectInputStream oStream;
        try{
            f = new File("studyOutput.dat");            //load file 
            inputStream = new FileInputStream(f);           //establish connection
            oStream = new ObjectInputStream(inputStream);       //data flows through the connection 
            
            study = (ArrayList<Study>) oStream.readObject();          //read obejct in the arrayList
            oStream.close();                                       //terminate the connection
            
        }catch(IOException | ClassNotFoundException e){
            System.out.println(e);
        }
        
        for(Study s : study){
            JOptionPane.showMessageDialog(null, "Automatic Loading : " + "\n"+ s.getDetails());
        }
    
    }

