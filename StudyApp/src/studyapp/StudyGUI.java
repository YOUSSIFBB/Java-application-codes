    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        
        if(personalRB.isSelected()){
            Personal p = new Personal();
            p.setDay(dayTf.getText());
            p.setTime(timeTf.getText()); 
            p.setDuration(Double.parseDouble(durationTf.getText())); 
            p.setModule(moduleTf.getText());
            p.setStudyTopic(studyTopicTf.getText());
            study.add(p);
        }else if(groupRB.isSelected()){
            Group g = new Group();
            g.setDay(dayTf.getText());
            g.setTime(timeTf.getText()); 
            g.setDuration(Double.parseDouble(durationTf.getText())); 
            g.setModule(moduleTf.getText());
            g.setTeamMates(teamMatesTf.getText());
            g.setAssesmentName(assesmentNameTf.getText());
            study.add(g); 
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void displayBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayBtnActionPerformed
        // TODO add your handling code here:
    
        if(study.isEmpty()){  
            //print this message if the array is empty
            JOptionPane.showMessageDialog(null, "Sorry the database is empty !");  
        }else{ 
            for(Study s: study){  
                //loop through the content of the array list using a for loop
                JOptionPane.showMessageDialog(null, s.getDetails());
            }//for loop

        }//else
    }//GEN-LAST:event_displayBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        if(study.isEmpty()){
            //print this message if the arrayList is empty
            JOptionPane.showMessageDialog(null, "unable to delete, the data base is empty !"); 
        }else{
            //allow the user to enter the module name to delete the data
            String search = JOptionPane.showInputDialog(null, "Enter the module name for the data you would like to delete");
            for(int i = 0; i < study.size(); i++){
                Study s = study.get(i); //get the index from the arrayList
                
                if(s.getModule().equalsIgnoreCase(search)){
                    study.remove(s);
                    i = i -1; 
                    JOptionPane.showMessageDialog(null, "Data deleted successfully");
                    break;
                }//inner if 
                
            }//for loop
        
        }//else
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        File f; 
        FileOutputStream fStream; 
        ObjectOutputStream oStream;
        
        try{
            f = new File ("studyOutput.dat");         //create the file
            fStream = new FileOutputStream(f);           //prepare to send the file 
            oStream = new ObjectOutputStream(fStream);    //send the file  
            oStream.writeObject(study);                    //write the data in that file 
            oStream.close();                                 //terminate the connection
        
        }catch(IOException e){
            System.out.println(e);           //print out the errors 
        }
        
    }//GEN-LAST:event_saveBtnActionPerformed

